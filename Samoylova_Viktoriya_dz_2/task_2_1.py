# 1. Выяснить тип результата выражений:
# 15 * 3
# 15 / 3
# 15 // 2
# 15 ** 2

multiplication = ['15 * 3']
print(isinstance(multiplication, list))

division_with_remainder = ['15 / 3']
print(isinstance(division_with_remainder, list))

integer_division = ['15 // 2']
print(isinstance(integer_division, list))

exponentiation = ['15 ** 2']
print(isinstance(exponentiation, list))